//Normal Function
function add(x,y){
   return x+y;
}
//anonymous function expression
let addTwoNumbers = function(x,y){
    return x+y;
}

//named function expression 
let addTwoNumbersAgain= function eval(x,y){
    return x+y;
}

//function calling
console.log(add(3,4));
console.log(addTwoNumbers(5,6));
console.log(addTwoNumbersAgain(7,8));